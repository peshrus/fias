--"Почтовый индекс" содержит почтовый индекс предприятия почтовой связи, обслуживающего данный адресный объект. Длина почтового индекса – 6 разрядов.
--"Код ИФНС ФЛ" и "Код ИФНС ЮЛ" содержат коды инспекций ФНС России по ведомственному ФНС России классификатору "Система обозначений налоговых органов" (СОНО), обслуживающих соответственно физических и юридических лиц на территории, на которой расположен данный адресный объект. Длина кода – 4 разряда.
--"Код территориального участка ИФНС ФЛ" и "Код территориального участка ИФНС ЮЛ" содержат коды территориальных участков* (упраздненных инспекций, преобразованных в подразделения межрайонных инспекций: отделы, территориальные участки и т.п.) ИФНС России по ведомственному справочнику кодов обозначений налоговых органов для целей учета налогоплательщиков (СОУН), обслуживающих соответственно физических и юридических лиц на территории, на которой расположен данный адресный объект. Длина кода – 4 разряда.
--"Код ОКАТО" содержит код объекта административно-территориального деления по общероссийскому классификатору ОКАТО соответствующего уровня,  на территории которого находится дом, строение, земельный участок (от субъекта РФ до сельского населенного пункта и внутригородских районов или внутригородских округов). Длина кода – 11 разрядов (заполняются все 11 разрядов).
--"Код ОКТМО" содержит код муниципального образования по Общероссийскому классификатору территорий муниципальных образований, на территории которого расположен адресуемый объект. Длина кода 8 байт.
--"Дата и время внесения записи" определяет дату и время внесения записи в БД.
--"Номер дома (земельного участка), корпуса, строения" содержат номер домов, корпусов, строений, земельных участков.
--"Признак владения" принимает следующие значения:
--1 – владение;
--2 – дом;
--3 – домовладение;
--4 – участок.
--"Номер корпуса" содержат номер корпуса.
--"Номер строения" содержат номер строения.
--"Признак строения" принимает следующие значения:
--1 – строение;
--2 – сооружение;
--3 – литер.
--"Идентификатор записи дома" используется для связи с нижестоящими адресными объектами.
--"Уникальный идентификатор записи дома" описан в пункте 2.1.1.2
--"Глобальный уникальный идентификатор дома" описан в пункте 2.1.1.2
--"Идентификатор объекта родительского объекта (города, населенного пункта, улицы и т.п.)" содержит идентификационный код адресного объекта по классификатору адресообразующих элементов, на территории которого находится дом.
--"Дата начала записи" определяет дату начала действия записи.
--"Дата окончания действия записи" определяет дату окончания действия записи.
--"Состояние дома" характеризует текущее состояние дома. Содержит коды состояния домов, используемые в БТИ.

