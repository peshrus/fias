--OKATO	OKATO	A	T(=11)	Н
--OKTMO	OKTMO	A	T(=8)	Н
--Дата время внесения (обновления) записи	UPDATEDATE	A	 	О	Типовой элемент <xs:date>
--Номер дома	HOUSENUM	A	T(1-20)	Н
--Признак владения	ESTSTATUS	A	N(1)	О
--Номер корпуса	BUILDNUM	A	T(0-10)	Н
--Номер строения	STRUCNUM	A	T(0-10)	Н
--Признак строения	STRSTATUS	A	N(10)	О
--Уникальный идентификатор записи дома	HOUSEID	A	T(=36)	О
--Глобальный уникальный идентификатор дома	HOUSEGUID	A	T(=36)	О
--Guid записи родительского объекта (улицы, города, населенного пункта и т.п.)	AOGUID	A	T(=36)	О
--Начало действия записи	STARTDATE	A	 	О	Типовой элемент <xs:dateTime>
--Окончание действия записи	ENDDATE	A	 	О	Типовой элемент <xs:dateTime>
--Состояние дома	STATSTATUS	A	N(10)	О
--Внешний ключ на нормативный документ	NORMDOC	A	T(=36)	Н
