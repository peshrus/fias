CREATE TABLE HOUSE (
--Почтовый индекс	POSTALCODE	A	T(=6)	Н
--Код ИФНС ФЛ	IFNSFL	A	T(=4)	Н
--Код территориального участка ИФНС ФЛ	TERRIFNSFL	A	T(=4)	Н
